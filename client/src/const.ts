// Generate login URL at runtime so redirect URI reflects the current origin.
export const getLoginUrl = (returnPath?: string) => {
  const oauthPortalUrl = import.meta.env.VITE_OAUTH_PORTAL_URL;
  const appId = import.meta.env.VITE_APP_ID;
  const origin = window.location.origin;
  const redirectUri = `${origin}/api/oauth/callback`;
  // Encode origin + optional return path in state
  const statePayload = returnPath
    ? JSON.stringify({ origin, returnPath })
    : origin;
  const state = btoa(statePayload);

  const base = oauthPortalUrl || origin; // fallback so dev doesn't crash
  const url = new URL(`/app-auth`, base);  url.searchParams.set("appId", appId);
  url.searchParams.set("redirectUri", redirectUri);
  url.searchParams.set("state", state);
  url.searchParams.set("type", "signIn");

  return url.toString();
};
